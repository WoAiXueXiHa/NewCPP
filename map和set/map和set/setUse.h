#pragma once
#include <iostream>
#include <set>
using namespace std;


void testSet() {
	// 1. 插入+遍历
	set<int> s;
	s.insert(2);
	s.insert(5);
	s.insert(8);
	s.insert(3);
	s.insert(9);
	s.insert(11);
	s.insert(7);
	
	set<int>::iterator it = s.begin();
	while (it != s.end()) {
		cout << *it << " ";
		++it;
	}
	cout << endl;
	// 不能插入重复的值
	s.insert(7);
	cout << "插入重复值7：\n";
	for (const auto& e : s) cout << e << " ";

	cout << "逆序遍历：\n";
	set<int>::reverse_iterator rit = s.rbegin();
	while (rit != s.rend()) {
		cout << *rit << " ";
		++rit;
	}

	cout << endl;

	// 2. 删除
	int x = 0;
	cin >> x;
	auto pos = s.find(x);
	if (pos != s.end()) s.erase(pos);
	else cout << x << "不存在！\n";
	cout << "删除后的序列：\n";
	for (const auto& e : s) cout << e << " ";

}
